print(set([1, 2, 3, 1, 3, 4])) # Conjuntos (sets) são coleções não ordenadas de elementos únicos
 # Conjuntos são úteis para eliminar duplicatas e realizar operações matemáticas como união, interseção e diferença

print(set('abacaxi')) # Conjuntos são mutáveis, então podemos adicionar ou remover elementos

print(set(('palio', 'gol', 'celta', 'palio'))) # Conjuntos não permitem elementos duplicados, então 'palio' só aparece uma vez