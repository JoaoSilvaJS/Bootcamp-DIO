numeros = set([1, 2, 3, 1, 3, 4])  # Conjuntos (sets) são coleções não ordenadas de elementos únicos
print(numeros)

letras = set('abacaxi')  # Conjuntos são mutáveis, então podemos adicionar ou remover elementos
print(letras)

carros = set(('palio', 'gol', 'celta', 'palio'))  # Conjuntos não permitem elementos duplicados, então 'palio' só aparece uma vez
print(carros)

linguagens = set(['python', 'java', 'c++', 'python'])  # Conjuntos não permitem elementos duplicados
print(linguagens)