# Operadores de comparação
Saldo = 450
Saque = 200

print('Saldo: ', Saldo)
print('Saque: ', Saque)
print()

print('Igualdade: Saldo == Saque = ', Saldo == Saque)
print('Diferença: Saldo != Saque = ', Saldo != Saque)
print('Maior que / maior ou igual: Saldo >= Saque = ', Saldo >= Saque)
print('Menor que / menor ou igual: Saldo <= Saque = ', Saldo <= Saque)